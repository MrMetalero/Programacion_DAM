package ejercicios_adicionales_t5;

public class Ejercicio3 {
    
    public static void main(String[] args) {
        
        int controlNumeros100 = 0;
        

        while (controlNumeros100 < 100) {
            if (controlNumeros100 % 2 == 0 || controlNumeros100 % 3 == 0) {
                System.out.println(controlNumeros100);
            } 
            controlNumeros100 += 1;
        }
       




    }
}
